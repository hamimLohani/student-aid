export function getMemberTypeLabel(memberType: string, language: Language) {
  const normalized =
    memberType === "Senior Member" ||
    memberType === "Junior Member" ||
    memberType === "Locals"
      ? memberType
      : null;

  if (normalized) return memberTypeLabels[normalized][language];
  return memberType;
}

